export const translateMuscleGroups = (muscleGroupsArray) => {
  if (!muscleGroupsArray || !Array.isArray(muscleGroupsArray) || muscleGroupsArray.length === 0) {
    return '—';
  }
  return muscleGroupsArray
    .map(m => muscleGroupTranslation[m.toLowerCase()] || m)
    .join(' • ');  // ← AQUI JÁ VEM COM O PONTO BONITINHO
};